<template>
    <FormWrapper :is-loading="isLoading" >
        <!-- <PageTitle :title="pageTitle" slot="title" :link="`/help/${processId}`"/> -->
        <PageTitle :title="PageTitle" slot="title" />
        <DataTable :actions="true"
            :data="Companies"
            :columns="tableColumns"
            :is-loading="isLoading" 
            @on-selection-change="selectChange"
            @on-page-change="pageChange"
            :pagination="pagination"
            @on-sort-change="sortChange">
        </DataTable>
        <ButtonGroup>
            <FormButton type="primary" @click="goBack()" left-icon="ios-arrow-back">Back to List</FormButton>
        </ButtonGroup>
    </FormWrapper>
</template>

<script>
    import FormWrapper from 'Components/form/FormWrapper';
    import PageTitle from 'Components/layout/PageTitle';
    import loadingMixin from 'Mixins/loadingMixin';
    import { getCompanyLogs } from "../api";
    import DataTable from 'Components/DataTable';
    import DateUtil from 'Utils/dateUtil'

    export default {
        name: "Single",
        mixins: [loadingMixin],
        data() {
            return {
                id: null,
                mode: null,
                PageTitle: 'Company Log',
                Companies: [],
                tableColumns: [
                    {
                        title: 'Activity',
                        key: 'Activity',
                        width: 600
                    },
                    {
                        title: 'Date',
                        key:'ActivityDate',
                        sortable: true,
                        render: (h, params) => {
                            if( params.row.ActivityDate == '' || params.row.ActivityDate == null){
                                return '';
                            }else{
                                return h('span', DateUtil.formatDateWithSlash(params.row.ActivityDate));
                            }
                        },
                    }
                ],
                sorting: {
                    key: 'id',
                    order: 'desc'
                },
                pagination: {
                    perPage: 10,
                    page: 1,
                    total: 0
                },
            }
        },
        created() {
            this.id = this.$route.params.id;
            this.getDataByFilter();
        },
        components: {
            FormWrapper,
            PageTitle,
            DataTable
        },
        methods: {
            sortChange(data) {
                this.sorting.key = data.key;
                this.sorting.order = data.order;
                this.getDataByFilter()
            },
            pageChange(page) {
                this.pagination.page = page;
                this.getDataByFilter()
            },
            selectChange(data) {
                this.selectedRows = data;
            },
            getDataByFilter() {
                this.showLoader();
                const { key, order } = this.sorting;
                const perPage = this.pagination.perPage;
                const page = this.pagination.page;
                const filters = {};
                filters.id = this.id;
                const data = {
                    filterjson: {
                        filter: [filters],
                        sort: [{ key, order }],
                        paging: [
                            {
                                startindex: perPage * (page - 1) + 1,
                                pagesize: perPage
                            }
                        ]
                    }
                };
                getCompanyLogs(data)
                    .then(this.handleResponse)
            },
            handleResponse(response) {
                this.Companies = response.data
                this.hideLoader();
            },
            goBack(){
                this.$router.push({ path: '/companyPublic' })
            }
        }
    }
</script>

